// Get the best matching language
function detectLanguage() {
    // Check URL param first
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang && translations[urlLang]) return urlLang;

    // Check localStorage
    const savedLang = localStorage.getItem('oh-lang');
    if (savedLang && translations[savedLang]) return savedLang;

    // Detect browser language
    const browserLangs = navigator.languages || [navigator.language || navigator.userLanguage];
    for (const lang of browserLangs) {
        const code = lang.split('-')[0].toLowerCase();
        if (translations[code]) return code;
    }

    return 'en';
}

// Apply translations to the page
function applyTranslation(lang) {
    const t = translations[lang];
    if (!t) return;

    // Fallback to English for missing keys
    const fallback = translations['en'];

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = t[key] || fallback[key];
        if (text) {
            // If the text contains HTML tags, use innerHTML
            if (text.includes('<')) {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
        }
    });

    // Translate alt attributes for images
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        const key = el.getAttribute('data-i18n-alt');
        const text = t[key] || fallback[key];
        if (text) {
            el.setAttribute('alt', text);
        }
    });

    // Handle RTL
    if (rtlLanguages.includes(lang)) {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.classList.add('rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.classList.remove('rtl');
    }

    // Update html lang attribute
    document.documentElement.setAttribute('lang', lang);

    // Save preference
    localStorage.setItem('oh-lang', lang);

    // Update URL with lang parameter (no page reload)
    const url = new URL(window.location);
    if (lang === 'en') {
        url.searchParams.delete('lang'); // Clean URL for English (default)
    } else {
        url.searchParams.set('lang', lang);
    }
    window.history.replaceState({}, '', url);

    // Update the language selector button text
    const langBtn = document.getElementById('langSelectorBtn');
    if (langBtn) {
        const flagSpan = langBtn.querySelector('.lang-flag');
        if (flagSpan) {
            flagSpan.textContent = languageNames[lang]?.split(' ')[0] || '🌐';
        }
    }
}

function createLanguageSelector() {
    const selectorHTML = `
    <div id="langSelector" class="fixed top-[6px] right-14 sm:right-4 z-[65] flex items-center">
        <button id="langSelectorBtn" class="flex items-center gap-1.5 bg-black/60 hover:bg-blood/30 backdrop-blur-md border border-white/10 hover:border-blood/50 rounded-full px-3 py-1.5 transition-all duration-300 cursor-pointer group">
            <span class="lang-flag text-sm">🌐</span>
            <span class="text-[9px] font-display tracking-widest text-gray-400 group-hover:text-white transition-colors uppercase" data-i18n="lang-label">LANG</span>
            <i class="fas fa-chevron-down text-[7px] text-gray-500 group-hover:text-white transition-colors"></i>
        </button>
        <div id="langDropdown" class="absolute top-full right-0 mt-2 bg-cosmic/95 backdrop-blur-xl border border-white/10 rounded-xl overflow-hidden shadow-[0_20px_60px_rgba(0,0,0,0.8)] w-40 sm:w-44 opacity-0 invisible translate-y-2 transition-all duration-300">
            <div class="py-1 max-h-[60vh] overflow-y-auto scrollbar-thin">
                ${Object.entries(languageNames).map(([code, name]) => `
                    <button class="lang-option w-full text-left px-4 py-2.5 text-xs font-display tracking-wider text-gray-400 hover:text-white hover:bg-blood/20 transition-all duration-200 flex items-center gap-2" data-lang="${code}">
                        <span>${name}</span>
                    </button>
                `).join('')}
            </div>
        </div>
    </div>`;

    document.body.insertAdjacentHTML('beforeend', selectorHTML);

    // Toggle dropdown
    const btn = document.getElementById('langSelectorBtn');
    const dropdown = document.getElementById('langDropdown');

    btn.addEventListener('click', (e) => {
        e.stopPropagation();
        const isVisible = dropdown.classList.contains('!opacity-100');
        if (isVisible) {
            dropdown.classList.remove('!opacity-100', '!visible', '!translate-y-0');
        } else {
            dropdown.classList.add('!opacity-100', '!visible', '!translate-y-0');
        }
    });

    // Close on outside click
    document.addEventListener('click', () => {
        dropdown.classList.remove('!opacity-100', '!visible', '!translate-y-0');
    });

    // Language option click
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.addEventListener('click', () => {
            const lang = opt.getAttribute('data-lang');
            applyTranslation(lang);
            dropdown.classList.remove('!opacity-100', '!visible', '!translate-y-0');

            // Highlight active
            document.querySelectorAll('.lang-option').forEach(o => o.classList.remove('!text-blood', '!bg-blood/10'));
            opt.classList.add('!text-blood', '!bg-blood/10');
        });
    });
}

// Initialize i18n system
function initI18n() {
    createLanguageSelector();
    const lang = detectLanguage();
    applyTranslation(lang);

    // Highlight current language in dropdown
    const activeOpt = document.querySelector(`.lang-option[data-lang="${lang}"]`);
    if (activeOpt) {
        activeOpt.classList.add('!text-blood', '!bg-blood/10');
    }
}

// Auto-init when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}
